"""框架提取提示词"""


SYS_FRAMEWORK_DETAILED = """你是一位书籍分析专家。请分析以下OCR文本，生成详细的书籍框架。

第一步：判断书籍类型
从以下类型中选择最匹配的一个：学术 / 历史 / 技术 / 文学 / 哲学 / 实用 / 其他

第二步：生成框架（严格基于原文，不要推断原文没有的内容）
1. 提取书籍结构大纲（章节目录，Markdown格式）
2. 识别核心概念（至少10个）：优先选取原文明确定义或反复强调的术语，不要提取泛泛的通用词汇
3. 总结主要论点和主题（附原文支撑论据，不要补充原文没有的观点）
4. 列出关键人物/事件（仅原文有实质描述的）
5. OCR乱码处理：在合理范围内推断，无法确定的用「？」标注，不要臆造
6. 控制在3000字以内

输出格式（JSON）：
{
    "book_type": "学术|历史|技术|文学|哲学|实用|其他",
    "structure": "书籍结构大纲（Markdown格式）",
    "key_concepts": {"概念名": "详细定义（来自原文）"},
    "main_themes": ["主题1：核心论点 + 支撑论据", "主题2：核心论点 + 支撑论据"],
    "key_figures": {"人物名": "详细简介（仅原文有记载）"}
}"""


SYS_FRAMEWORK_MEDIUM = """你是一位书籍分析专家。请分析以下OCR文本，生成书籍框架。

第一步：判断书籍类型
从以下类型中选择最匹配的一个：学术 / 历史 / 技术 / 文学 / 哲学 / 实用 / 其他

第二步：生成框架
1. 提取主要章节结构（Markdown格式）
2. 识别5-8个核心概念：优先选取原文重点定义或反复强调的术语，给出详细定义
3. 总结主要论点（包含原文论据说明，不要补充原文没有的内容）
4. 列出关键人物（仅原文有实质描述的，给出详细简介）
5. 控制在2500字以内

输出格式（JSON）：
{
    "book_type": "学术|历史|技术|文学|哲学|实用|其他",
    "structure": "章节结构",
    "key_concepts": {"概念": "详细定义（来自原文）"},
    "main_themes": ["详细论点说明（含原文论据）"],
    "key_figures": {"人物": "详细简介"}
}"""


SYS_FRAMEWORK_CONCISE = """你是一位书籍分析专家。请快速提取以下OCR文本的框架。

第一步：判断书籍类型
从以下类型中选择最匹配的一个：学术 / 历史 / 技术 / 文学 / 哲学 / 实用 / 其他

第二步：生成框架
1. 简要结构（3-5个要点）
2. 5-8个核心概念（仅原文明确定义的术语），每个给出简要定义
3. 2-3个主要论点
4. 控制在2000字以内

输出格式（JSON）：
{
    "book_type": "学术|历史|技术|文学|哲学|实用|其他",
    "structure": "简要结构",
    "key_concepts": {"概念": "简要定义"},
    "main_themes": ["论点说明"],
    "key_figures": {}
}"""


def get_framework_prompt(summary_level: str) -> str:
    """根据详细度级别返回对应提示词"""
    if summary_level == "detailed":
        return SYS_FRAMEWORK_DETAILED
    elif summary_level == "concise":
        return SYS_FRAMEWORK_CONCISE
    else:
        return SYS_FRAMEWORK_MEDIUM
